// 배열과 같이 반복 가능한 객체를 전개 구문을 사용해서 확장합니다.
// 함수로 호출할 경우엔 0개 이상의 인수로 배열 리터럴에서는 요소로 확장할수 있습니다
// 객체 리터럴 같은 경우 0개 이상 키-값 쌍으로 확장할수 있습니다.


const nums = [1,2,3,4,5,123123,5,54,67,776]
// 배열이 펼치지고 인수가 따로따로 들어가집니다.
// 전개구문이 배열괄호를 지웁니다. 
Math.max(...nums) // 전개 구문을 사용하면 

Math.max(1,2,3,4,5,123123,5,54,67,776) // 와 같아집니다.